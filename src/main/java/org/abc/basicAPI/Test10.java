package org.abc.basicAPI;

public class Test10 {
    public static void main(String[] args) {
        System.out.println(Math.E);
        System.out.println(Math.PI);

        System.out.println(Math.abs(-12));//절댓값
        System.out.println(Math.ceil(12.5));//올림
        System.out.println(Math.floor(12.5));//내림
        System.out.println(Math.max(5,8));
        System.out.println(Math.min(5,8));
        System.out.println(Math.pow(2,3));//a의 b승 (power)
        System.out.println(Math.random());//0~1중 난수 생성
        System.out.println(Math.round(12.5));//소수점 첫째 반올림 (round off)
        System.out.println(Math.sqrt(4));//제곱근 (square root)
    }
}
